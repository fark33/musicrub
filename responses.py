def get_random_response(response_str: str) -> str:
    """اگر پاسخ شامل '/' بود، یک گزینه تصادفی انتخاب کن."""
    if "/" in response_str:
        options = [opt.strip() for opt in response_str.split("/")]
        return random.choice(options)
    return response_str
